    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        Patient newpat = new Patient(this.patientID);
        String upStatus = "not complete";
        newpat.updatePatientDetails(newpat.getPatientID(), newpat.getName(), newpat.getLastName(), newpat.getDOB(), upStatus);
        newpat.receivePatientData(this.patientID);
        String status = "Complete now: " + newpat.getStatus();
        System.out.println(status);
        Appointment newap = new Appointment(this.patientID);
        try {
            newap.deleteAllAppointments(this.patientID);
        } catch (SQLException ex) {
            Logger.getLogger(ViewAppointmentsPatient.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane jop1 = new JOptionPane("Treatment Terminated", JOptionPane.WARNING_MESSAGE);
        JDialog dialog = jop1.createDialog(status);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        ViewAppointmentsPatient vap = new ViewAppointmentsPatient(patientID);
        vap.setVisible(true);
        setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton3MouseClicked

