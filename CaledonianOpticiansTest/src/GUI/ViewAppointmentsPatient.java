    public void Populate(String patientID){
        Connection conn;
        Statement statement;
        String getAppointments = "SELECT time, date, reference FROM appointments WHERE patientID='"+ patientID +"'";
        try {
            DBConnect db = new DBConnect();
            conn = db.Connect();
            statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(getAppointments);
            appointmentsTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(ViewAppointmentsPatient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

